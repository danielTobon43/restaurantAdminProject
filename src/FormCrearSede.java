    private void btnCrearSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearSedeActionPerformed

        if (Utilitaria.validarCadenaVacia(this.txtNombreSede.getText()) && Utilitaria.validarCadenaVacia(this.txtCodigoSede.getText()) && 
            Utilitaria.validarCadenaVacia(this.txtDireccionSede.getText()) && Utilitaria.validarCadenaVacia(this.txtNomAdmSede.getText()) &&
            Utilitaria.validarCadenaVacia(this.txtTelefonoSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar el:\n * Nombre\n"
                                                  + " * Código\n * Dirección\n * Nombre Administrador\n"
                                                  + " * Telefono ", "Atención");
            this.txtNombreSede.requestFocus();
        } else if (Utilitaria.validarCadenaVacia(this.txtNombreSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar el nombre de la sede", "Atención");
            this.txtNombreSede.requestFocus();

        } else if (Utilitaria.validarCadenaVacia(this.txtCodigoSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar el código de la sede", "Atención");
            this.txtCodigoSede.requestFocus();

        } else if (Utilitaria.validarCadenaVacia(this.txtDireccionSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar la dirección de la sede", "Atención");
            this.txtDireccionSede.requestFocus();
        } else if (Utilitaria.validarCadenaVacia(this.txtNomAdmSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar el nombre del administrador de la sede", "Atención");
            this.txtNomAdmSede.requestFocus();
        } else if (Utilitaria.validarCadenaVacia(this.txtTelefonoSede.getText())) {
            Utilitaria.imprimirEnVentanaInformativa("Debe digitar el telefono de la sede", "Atención");
            this.txtTelefonoSede.requestFocus();
        } else if(jCheckBtnActiva.isSelected() == false && jCheckBtnInactiva.isSelected() == false){
            Utilitaria.imprimirEnVentanaInformativa("Debe marcar si la sede está activa o inactiva", "Atención");
            
        }else {
           

            FormNuevaSede yeah = new FormNuevaSede();

            FormNuevaSede.jLabelNombreSede.setText(this.txtNombreSede.getText());
            FormNuevaSede.jLabelCodigoNuevaSede.setText(this.txtCodigoSede.getText());
            FormNuevaSede.jLabelDireccionNuevaSede.setText(this.txtDireccionSede.getText());
            FormNuevaSede.jLabelNomAdmNuevaSede.setText(this.txtNomAdmSede.getText());
            FormNuevaSede.jLabelTelefonoNuevaSede.setText(this.txtTelefonoSede.getText());
            FormNuevaSede.jCheckNuevaSedeEstadoActiva.setSelected(this.jCheckBtnActiva.isSelected());
            FormNuevaSede.jCheckNuevaSedeEstadoInactiva.setSelected(this.jCheckBtnInactiva.isSelected());
            
            yeah.setVisible(true);

            this.txtNombreSede.setText("");
            this.txtCodigoSede.setText("");
            this.txtDireccionSede.setText("");
            this.txtNomAdmSede.setText("");
            this.txtTelefonoSede.setText("");
        }


    }//GEN-LAST:event_btnCrearSedeActionPerformed

