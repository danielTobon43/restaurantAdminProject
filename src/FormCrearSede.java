    private void txtNombreSedeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreSedeKeyTyped
        char car = evt.getKeyChar();
if((car<'a' || car>'z') && (car<'A' || car>'Z')) evt.consume();
    }//GEN-LAST:event_txtNombreSedeKeyTyped

