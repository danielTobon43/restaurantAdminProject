    private void txtTelefonoSedeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoSedeKeyTyped
         if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtTelefonoSedeKeyTyped

